package com.slcupc.untity;

public class city {

	private int ID;
	private String city;
	private String lan;
	private String lon;

	public int getID() {
		return ID;
	}

	public void setID(int ID) {
		this.ID=ID;
	}
	
	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getLan() {
		return lan;
	}

	public void setLan(String lan) {
		this.lan = lan;
	}

	public String getLon() {
		return lon;
	}

	public void setLon(String lon) {
		this.lon = lon;
	}

	

}
